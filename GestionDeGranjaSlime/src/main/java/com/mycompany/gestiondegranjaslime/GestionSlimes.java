    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
//logica del boton guardar
        //obtenemos todos los datos que queremos guardar de los campos
        String Tipo = tipo.getText();
        String Dieta = dieta.getText();
        String ComidaFav = comidaFavorita.getText();
        String Plort = plort.getText();
        String Codigo = codigo.getText();
        //una vez traemos lo que vamos a usar, verificamos si esta vacio
        if(Tipo.isEmpty() || Dieta.isEmpty() ||ComidaFav.isEmpty() ||Plort.isEmpty() ||Codigo.isEmpty()){
            JOptionPane.showMessageDialog(this, "Complete los campos bien no sea burro", "ERROororoRORO:", JOptionPane.ERROR_MESSAGE);
            return;
        }
        //verifica que el ID colocado NO exista antes de guardarlo
        for(int i=0;i<SlimeID;i++){
         if(Codigo.equals(Slimes[i][4])){
        //si ya existe arroja un error sin limpiando SOLO el campo del ID para que sea mas facil volver a poner otro
           JOptionPane.showMessageDialog(this, "ESE ID YA EXISTE, ponga otro ;)", "ERROR FATAL:", JOptionPane.ERROR_MESSAGE);
           codigo.setText("");
           return;
        }   
}
        //si no existe guardamos normal
        //guardamos los datos colocandolos en nuestro array bidimensional ubicandolos con su ID como fila y en 5 columnas distintas
        Slimes[SlimeID][0] = Tipo;
        Slimes[SlimeID][1] = Dieta;
        Slimes[SlimeID][2] = ComidaFav;
        Slimes[SlimeID][3] = Plort;
        Slimes[SlimeID][4] = Codigo;
        //mensaje de alerta que avise que se guardo bien (aunque siempre sale aun si no se guardo bien)
        JOptionPane.showMessageDialog(this, "Registro Guardado Existosamente!");
        //aumentamos el id en 1 para que la siguiente carga lo haga en la siguiente fila
        SlimeID++;
        //colocamos todo en blanco para hacer mas facil la siguiente carga
        tipo.setText("");
         dieta.setText("");
          comidaFavorita.setText(""); 
           plort.setText("");
            codigo.setText("");
       
    }//GEN-LAST:event_guardarActionPerformed
        
    private void mostrarTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarTodosActionPerformed
    // limpiar campos y text area
listaSlime.setText("");
codigoBuscar.setText("");

// recorrer todas las filas hasta SlimeID para verificar si es null
for (int m = 0; m < SlimeID; m++) {
    boolean filaVacia = true;
    
    // verificar si la fila COMPLETA es null 
    for (int n = 0; n < 4; n++) {
        if (Slimes[m][n] != null) {
            filaVacia = false;
            break;
        }
    }
    
    // Si la fila no es null se muestra
    if (!filaVacia) {
        listaSlime.append("\n");
        for (int n = 0; n < 4; n++) {
            if (Slimes[m][n] != null) {
                listaSlime.append(n + 1 + "- " + Slimes[m][n] + "\n");
            }
        }
    }
}
    
    }//GEN-LAST:event_mostrarTodosActionPerformed

