    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
    //logica del boton buscar
    int Encontrado = 0;
    String buscador = codigoBuscar.getText();
    listaSlime.setText("");
    /*
    El bucle itera sobre el ID de los registros para alcanzar el total de estos, toma la variable "buscador"
    para compararlo por cada iteracion para ver si coincide con un codigo en los registros, si es asi
    lo agrega al TextArea con un append para mostrarlo
    */
    for(int n =0;n<SlimeID;n++){
        if(buscador.equals(Slimes[n][4])){
            for(int m =0;m<4;m++){
         listaSlime.append(m+1 + "- " + Slimes[n][m] + "\n");
            }
            //rompe el bucle y coloca la bandera en 1
            Encontrado = 1;
            return;
        }
    }
    //si no se encuentrar coincidencias da un error y limpia el campo
    if(Encontrado == 0){
        JOptionPane.showMessageDialog(this, "No existen registros con ese ID asociado");
        listaSlime.setText("");
    }
    }//GEN-LAST:event_buscarActionPerformed

    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        //logica del btom borrar
        // no se, le borro el id entonces ya no existe jiji 
        String eliminador = codigoBuscar.getText();
        
        for(int n =0;n<SlimeID;n++){
        if(eliminador.equals(Slimes[n][4])){
           Slimes[n][0] = null;
           Slimes[n][1] = null;
           Slimes[n][2] = null;
           Slimes[n][3] = null;
           Slimes[n][4] = null;
           JOptionPane.showMessageDialog(this, "Se elimino el registro de forma exitosa ;)");
           listaSlime.setText("");
           codigoBuscar.setText("");
        }
    }
    
        
    }//GEN-LAST:event_eliminarActionPerformed

